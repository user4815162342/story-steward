var scriptPath = arguments[0];
if (scriptPath.indexOf("/") == -1) {
    scriptPath = scriptPath.replace(/\\/g, "/");
}
var isDevTarget = arguments[1] == 1;
var isRelTarget = arguments[2] == 1;

buildScriptsPath = typeof buildScriptsPath == "undefined" ? scriptPath + "../vendor/dojo/util/buildscripts/" : buildScriptsPath;
load(buildScriptsPath + "jslib/fileUtil.js");
load(scriptPath + "fixFileUtil.js")
load(buildScriptsPath + "jslib/logger.js");
load(scriptPath + "info.js");

var AppInfoText = 
"/* This file is automatically generated */\n" + 
"ApplicationInfo = {\n" + 
"	Title: \"" + AppInfo.Title + "\",\n" + 
"	Name: \"" + AppInfo.Name + "\",\n" + 
"	ID: \"" + AppInfo.ID + "\",\n" + 
"	Version: {\n" + 
"		Major: \"" + AppInfo.Version.Major + "\",\n" + 
"		Minor: \"" + AppInfo.Version.Minor + "\",\n" + 
"		Revision: \"" + AppInfo.Version.Revision + "\"\n" + 
"	}\n" + 
"}\n";

if (isDevTarget) {
	
	logger.info("Copying test to development");
    fileUtil.copyDir(scriptPath + "../test/", scriptPath + "../development/", /^(?:.*\.(?:html|js|htm|css|json|txt))|LICENSE|NOTICE$/g, true);
	logger.info("Copying source to development");
    fileUtil.copyDir(scriptPath + "../source/", scriptPath + "../development/", /^(?:.*\.(?:html|js|htm|css|txt))|LICENSE|NOTICE$/g, true);
	logger.info("Copying resources to development");
    fileUtil.copyFile(scriptPath + "../resources/icons/icons.png", scriptPath + "../development/resources/icons.png", true);
    fileUtil.copyFile(scriptPath + "../resources/icons/readme.txt", scriptPath + "../development/resources/readme.txt", true);
    fileUtil.copyFile(scriptPath + "../resources/quotes.js", scriptPath + "../development/resources/quotes.js", true);
	logger.info("Copying non-dojo vendor to development");
    fileUtil.copyFile(scriptPath + "../vendor/Math.uuid.js", scriptPath + "../development/js/Math.uuid.js", true);
    logger.info("Copying version to development");
	fileUtil.saveFile(scriptPath + "../development/js/appinfo.js",AppInfoText);
}
if (isRelTarget) {
	logger.info("Copying source to release");
    fileUtil.copyDir(scriptPath + "../source/", scriptPath + "../release/", /^(?:.*\.(?:html|js|htm|css|txt))|LICENSE|NOTICE$/g, true);
	logger.info("Copying resources to release");
    fileUtil.copyFile(scriptPath + "../resources/icons/icons.png", scriptPath + "../release/resources/icons.png", true);
    fileUtil.copyFile(scriptPath + "../resources/icons/readme.txt", scriptPath + "../release/resources/readme.txt", true);
    fileUtil.copyFile(scriptPath + "../resources/quotes.js", scriptPath + "../release/resources/quotes.js", true);
	logger.info("Copying non-dojo vendor to release");
    fileUtil.copyFile(scriptPath + "../vendor/Math.uuid.js", scriptPath + "../release/js/Math.uuid.js", true);
    logger.info("Copying version to release");
	fileUtil.saveFile(scriptPath + "../release/js/appinfo.js",AppInfoText);
}
logger.info("Files copied.")
